# Hash
# hash()

# this func. compare the key of dictionary with the given key

# if it is match then it will return the value 
# it is only applicable for immutable data types
key="string"
print(hash(key))
# -3930329188287470290

num_hash=hash((1,3))
#  This hash value can be used for various purposes
# like indexing data structures such as dictionaries or sets for faster access.
print(num_hash)
# -1440771752368011620